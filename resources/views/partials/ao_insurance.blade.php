    <h2 class="p-2">Osiguranje od AO</h2>

    <p>Iako je bezbednost imperativ u saobraćaju, brojne su okolnosti koje mogu dovesti do neželjenih, a u isto vreme i neprijatnih događaja.
        Upravo zato, najvažnije je da uz osnovna vozačka dokumenta, sa sobom uvek imate polisu osiguranja od autoodgovornosti.</p>

    <p>Osiguranje od auto-odgovornosti je zakonski obavezno osiguranje vozila i bez njega se ne može izvršiti registracija.
        Ovim je pokrivena odgovornost vlasnika, odnosno korisnika, kada upotrebom motornog vozila pričini štetu trećim licima usled telesne povrede,
        narušavanja zdravlja, smrti, uništenja ili oštećenja stvari.</p>

    <p>Ovom polisom ste zaštićeni ukoliko svojim vozilom pričinite štetu trećem licu i ukoliko do štete dođe osiguravajuća kuća će umesto Vas pokriti nastale troškove u visini osigurane sume.</p>

    <p>Iznos najniže sume osiguranja na koju može biti ugovoreno osiguranje od auto-odgovornosti utvrđuje Vlada na predlog Narodne banke Srbije:</p>
    <ul class="listNone p-1 p-lg-2">
        <li class="p-2">- za štetu na licima, po jednom štetnom događaju, bez obzira na broj oštećenih lica 1.000.000 evra;</li>
        <li class="p-2">- za štetu na stvarima, po jednom štetnom događaju, bez obzira na broj oštećenih lica 200.000 evra.</li>
    </ul>

    <p>Polisa obaveznog osiguranja od auto-odgovornosti se zaključuje na godinu dana i vezana je isključivo za vozilo, a ne za vozača.
        To znači da će svaka šteta biti pokrivena bez obzira na to ko upravlja vozilom i da će osiguravajuća kuća umesto Vas nadoknaditi sve materijalne i nematerijalne štete koje pretrpe treća lica,
        ako do saobraćajne nesreće dođe vašom krivicom. Ukoliko pretrpite štetu od strane motornog vozila (putničko, teretno vozilo, trolejbus, autobus, tramvaj, priključno vozilo, motocikl itd.),
        imate pravo na naknadu štete od društva za osiguranje kod koga je to vozilo osigurano. Pravo na odštetu imaju sve osobe koje se nalaze u vozilu koje je izazvalo saobraćajnu nezgodu,
        kao i sve osobe koje se nalaze u drugom vozilu koje učestvuje u saobraćajnoj nezgodi.
    </p>

    <p>U skladu sa važećim Zakonom o saobraćaju, svi vozači motornih vozila u obavezi su da kod sebe imaju Izveštaj o saobraćajnoj nezgodi.
        Koristi se u slučaju manjih saobraćajnih nezgoda, sa manjom materijalnom štetom, i može biti validan umesto policijskog izveštaja.</p>


