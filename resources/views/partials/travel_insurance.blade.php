    <h1 class="p-2">Putno osiguranje</h1>

    <p>Putno osiguranje je jedno od najrasprostranjenijih osiguranja koje se ugovara u Srbiji. Odnos pokrića i cene je u korist klijenta pa tako za manje od 100 RSD po danu,
        klijent može da dobije zaštitu u slučaju nemilih događaja u inostranstvu</p>
    <p>Putno osiguranje pokriva sledeće rizike:</p>
    <ul class="listNone">
        <li>- Troškovi zdravstvene zaštite</li>
        <li>- Gubitak prtljaga – ugovara se posebno</li>
    </ul>

    <h4 class="p-2">Polisa osiguranja može biti:</h4>
    <h4 class="p-2">1. Individualna</h4>
    <p>Ovom polisom osigurava se jedna osoba čije je ime naznačeno na polisi.</p>
    <p>Putno osiguranje vam pruža mogućnost da se osigurate i ukoliko je svrha putovanja privremeni boravak u inostranstvu radi školovanja.
        Ovu potvrdu/polisu mogu ugovoriti lica od 17 do 27 godina starosti, a trajanje osiguravajućeg pokrića je 365 dana u godini.</p>

    <h4 class="p-2">2. Grupna</h4>
    <p>Ovom polisom može biti obuhvaćeno 5 i više lica  (mogu biti uključeni i deca i odrasli). Polisu prati i spisak osiguranih osoba, iako ona glasi na prvu osobu sa spiska.
        Da bi polisa bila važeća neophodno je pre puta poslati spisak osiguranika.</p>

    <h4 class="p-2">3. Porodična</h4>
    <p>Ovom potvrdom/polisom može se osigurati najmanje jedan roditelj/staratelj do navršenih 70 godina starosti i najviše petoro dece do navršenih 18 godina starosti, koji putuju zajedno i istovremeno.
        Deca mogu biti rođena u braku ili van braka, usvojena, pastorčad i deca uzeta na izdržavanje.</p>

    <h4 class="p-2">4. Multi polisa</h4>
    <p>U ponudi su i putovanja sa više izlazaka iz zemlje na period od 15, 30, 50, 60, 90 i 180 dana tokom jedne godine.
        Ugovoreni broj dana osiguravajućeg pokrića može se iskoristiti u kontinuitetu ili sa više ulazaka/izlazaka iz zemlje.</p>


